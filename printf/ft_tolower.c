#include "ft_printf.h"

char	*ft_tolower(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (c[i] >= 'A' && c[i] <= 'Z')
			c[i] += ('a' - 'A');
		i++;
	}
	return (c);
}
